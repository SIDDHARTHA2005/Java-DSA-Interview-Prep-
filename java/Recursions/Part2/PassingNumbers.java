package Recursions.Part2;

public class PassingNumbers {
    public static void main(String[] args) {
        int n = 5;
        int m = 5;
        System.out.println(n--);
        System.out.println(n--);
        System.out.println(n);
        System.out.println("---");
        System.out.println(--m);
        System.out.println(--m);
        System.out.println(m);
    }
}
